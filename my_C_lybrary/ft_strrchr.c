#include "libft.h"

char	*ft_strrchr(const char *str, int c)
{
	int	i;

	i = ft_strlen(str);
	while (i > 0)
	{
		if ((char)c == str[i])
			return ((char *)str + i);
		i--;
	}
	return (0);
}
